"""
Confidence Scorer
-----------------
Assigns a trust score (0.0–1.0) to every field extracted from every source.

Design philosophy:
  - Structured sources (CSV, ATS) get higher baseline confidence than
    unstructured ones (GitHub bio, scraped pages).
  - LLM-extracted data from resumes gets its own tier ("resume_llm") —
    high confidence because the model has full document context.
  - Certain fields are inherently more reliable from certain sources
    (e.g., GitHub is the authoritative source for github_url, but a
    recruiter CSV is more reliable for phone numbers).
  - When multiple sources agree on a value, we can *boost* confidence —
    this is handled by the merger, not here.
  - Semantic similarity between two string field values from different
    sources can be used to apply a soft-agreement boost.
"""

from typing import Dict, Optional, List


